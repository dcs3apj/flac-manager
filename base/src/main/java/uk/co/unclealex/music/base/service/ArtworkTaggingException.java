package uk.co.unclealex.music.base.service;

public class ArtworkTaggingException extends Exception {

	public ArtworkTaggingException() {
		super();
	}

	public ArtworkTaggingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArtworkTaggingException(String message) {
		super(message);
	}

	public ArtworkTaggingException(Throwable cause) {
		super(cause);
	}

}
