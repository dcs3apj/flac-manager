package uk.co.unclealex.music.base.service;

public class ExternalCoverArtException extends Exception {

	public ExternalCoverArtException() {
		super();
	}

	public ExternalCoverArtException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExternalCoverArtException(String message) {
		super(message);
	}

	public ExternalCoverArtException(Throwable cause) {
		super(cause);
	}

}
